class ColorMapper(object):
    """
    I map floating-point values in the range 0.0 to 1.0 to RGB byte
    triplets.
    """
    def __init__(self):
        self.jMax = len(RGB) - 1

    def __call__(self, x):
        result = array('B')
        np.rint(self.jMax * x, x)
        for j in x.astype(np.uint16):
            result.extend(RGB[j,:])
        return result

    
